// add гэдэг int төрлийг буцаадаг а, b гэдэг хоёр параметр аваад түүнийг нэмээд буцаадаг функц
// бичнэ үү
int add(int a, int b) {
  return a + b;
}
// int хариу буцаадаг minus гэдэг нэртэй a, b гэсэн int төрөлтэй
// тэгээд өгөгдсөн параметрүүдийн ялгаврыг буцаадаг функц бичнэ үү.
int minus(int a, int b) {
  return a - b;
}